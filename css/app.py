num1 = int(input('Enter first no: '))
num2 = int(input('Enter second no: '))
op = input('Enter Operator: ')

if op=='+':
    num3=(num1+num2)
    print(num3)
elif op=='-':
    num3=(num1-num2)
    print(num3)
elif op=='*':
    num3=(num1*num2)
    print(num3)
elif op=='/':
    num3=(num1/num2)
    print(num3)